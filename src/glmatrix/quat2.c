Quat2 Quat2_rotateByQuatAppend(Quat2 out,Quat2 a,Quat q) {
  VecP qx = q[0],
    qy = q[1],
    qz = q[2],
    qw = q[3],
    ax = a[0],
    ay = a[1],
    az = a[2],
    aw = a[3];

  out[0] = ax * qw + aw * qx + ay * qz - az * qy;
  out[1] = ay * qw + aw * qy + az * qx - ax * qz;
  out[2] = az * qw + aw * qz + ax * qy - ay * qx;
  out[3] = aw * qw - ax * qx - ay * qy - az * qz;
  ax = a[4];
  ay = a[5];
  az = a[6];
  aw = a[7];
  out[4] = ax * qw + aw * qx + ay * qz - az * qy;
  out[5] = ay * qw + aw * qy + az * qx - ax * qz;
  out[6] = az * qw + aw * qz + ax * qy - ay * qx;
  out[7] = aw * qw - ax * qx - ay * qy - az * qz;
  return out;
}

Quat2 Quat2_rotateByQuatPrepend(Quat2 out,Quat2 q,Quat a) {
  VecP qx = q[0],
    qy = q[1],
    qz = q[2],
    qw = q[3],
    bx = a[0],
    by = a[1],
    bz = a[2],
    bw = a[3];

  out[0] = qx * bw + qw * bx + qy * bz - qz * by;
  out[1] = qy * bw + qw * by + qz * bx - qx * bz;
  out[2] = qz * bw + qw * bz + qx * by - qy * bx;
  out[3] = qw * bw - qx * bx - qy * by - qz * bz;
  bx = a[4];
  by = a[5];
  bz = a[6];
  bw = a[7];
  out[4] = qx * bw + qw * bx + qy * bz - qz * by;
  out[5] = qy * bw + qw * by + qz * bx - qx * bz;
  out[6] = qz * bw + qw * bz + qx * by - qy * bx;
  out[7] = qw * bw - qx * bx - qy * by - qz * bz;
  return out;
}

Quat2 Quat2_rotateAroundAxis(Quat2 out,Quat2 a,Vec3 axis,VecP rad) {
  //Special case for rad = 0
  if (fabsf(rad) == 0.0) {
    return Quat2_copy(out, a);
  }
  VecP axisLength = sqrt(axis[0]*axis[0]+axis[1]*axis[1]+axis[2]*axis[2]);

  rad = rad * 0.5;
  VecP s = sin(rad);
  VecP bx = s * axis[0] / axisLength;
  VecP by = s * axis[1] / axisLength;
  VecP bz = s * axis[2] / axisLength;
  VecP bw = cos(rad);

  VecP ax1 = a[0],
    ay1 = a[1],
    az1 = a[2],
    aw1 = a[3];
  out[0] = ax1 * bw + aw1 * bx + ay1 * bz - az1 * by;
  out[1] = ay1 * bw + aw1 * by + az1 * bx - ax1 * bz;
  out[2] = az1 * bw + aw1 * bz + ax1 * by - ay1 * bx;
  out[3] = aw1 * bw - ax1 * bx - ay1 * by - az1 * bz;

  VecP ax = a[4],
    ay = a[5],
    az = a[6],
    aw = a[7];
  out[4] = ax * bw + aw * bx + ay * bz - az * by;
  out[5] = ay * bw + aw * by + az * bx - ax * bz;
  out[6] = az * bw + aw * bz + ax * by - ay * bx;
  out[7] = aw * bw - ax * bx - ay * by - az * bz;

  return out;
}

Quat2 Quat2_add(Quat2 out,Quat2 a,Quat2 b) {
  out[0] = a[0] + b[0];
  out[1] = a[1] + b[1];
  out[2] = a[2] + b[2];
  out[3] = a[3] + b[3];
  out[4] = a[4] + b[4];
  out[5] = a[5] + b[5];
  out[6] = a[6] + b[6];
  out[7] = a[7] + b[7];
  return out;
}

Quat2 Quat2_multiply(Quat2 out,Quat2 a,Quat2 b) {
  VecP ax0 = a[0],
    ay0 = a[1],
    az0 = a[2],
    aw0 = a[3],
    bx1 = b[4],
    by1 = b[5],
    bz1 = b[6],
    bw1 = b[7],
    ax1 = a[4],
    ay1 = a[5],
    az1 = a[6],
    aw1 = a[7],
    bx0 = b[0],
    by0 = b[1],
    bz0 = b[2],
    bw0 = b[3];
  out[0] = ax0 * bw0 + aw0 * bx0 + ay0 * bz0 - az0 * by0;
  out[1] = ay0 * bw0 + aw0 * by0 + az0 * bx0 - ax0 * bz0;
  out[2] = az0 * bw0 + aw0 * bz0 + ax0 * by0 - ay0 * bx0;
  out[3] = aw0 * bw0 - ax0 * bx0 - ay0 * by0 - az0 * bz0;
  out[4] = ax0 * bw1 + aw0 * bx1 + ay0 * bz1 - az0 * by1 + ax1 * bw0 + aw1 * bx0 + ay1 * bz0 - az1 * by0;
  out[5] = ay0 * bw1 + aw0 * by1 + az0 * bx1 - ax0 * bz1 + ay1 * bw0 + aw1 * by0 + az1 * bx0 - ax1 * bz0;
  out[6] = az0 * bw1 + aw0 * bz1 + ax0 * by1 - ay0 * bx1 + az1 * bw0 + aw1 * bz0 + ax1 * by0 - ay1 * bx0;
  out[7] = aw0 * bw1 - ax0 * bx1 - ay0 * by1 - az0 * bz1 + aw1 * bw0 - ax1 * bx0 - ay1 * by0 - az1 * bz0;
  return out;
}

Quat2 Quat2_scale(Quat2 out,Quat2  a,VecP b) {
  out[0] = a[0] * b;
  out[1] = a[1] * b;
  out[2] = a[2] * b;
  out[3] = a[3] * b;
  out[4] = a[4] * b;
  out[5] = a[5] * b;
  out[6] = a[6] * b;
  out[7] = a[7] * b;
  return out;
}

VecP Quat2_dot(Quat2 a, Quat2 b){
    return a[0] * b[0] + a[1] * b[1] + a[2] * b[2]+ a[3] * b[3];
}


Quat2 Quat2_lerp(Quat2 out,Quat2 a,Quat2 b,VecP t) {
  VecP mt = 1 - t;
  if (Quat2_dot(a, b) < 0) t = -t;

  out[0] = a[0] * mt + b[0] * t;
  out[1] = a[1] * mt + b[1] * t;
  out[2] = a[2] * mt + b[2] * t;
  out[3] = a[3] * mt + b[3] * t;
  out[4] = a[4] * mt + b[4] * t;
  out[5] = a[5] * mt + b[5] * t;
  out[6] = a[6] * mt + b[6] * t;
  out[7] = a[7] * mt + b[7] * t;

  return out;
}

VecP Quat2_squaredLength(Quat2 a) {
  VecP x = a[0];
  VecP y = a[1];
  VecP z = a[2];
  VecP w = a[3];
  return x*x + y*y + z*z + w*w;
}

Quat2 Quat2_invert(Quat2 out,Quat2 a) {
  VecP sqlen = Quat2_squaredLength(a);
  out[0] = -a[0] / sqlen;
  out[1] = -a[1] / sqlen;
  out[2] = -a[2] / sqlen;
  out[3] = a[3] / sqlen;
  out[4] = -a[4] / sqlen;
  out[5] = -a[5] / sqlen;
  out[6] = -a[6] / sqlen;
  out[7] = a[7] / sqlen;
  return out;
}

Quat2 Quat2_conjugate(Quat2 out, Quat2 a) {
  out[0] = -a[0];
  out[1] = -a[1];
  out[2] = -a[2];
  out[3] = a[3];
  out[4] = -a[4];
  out[5] = -a[5];
  out[6] = -a[6];
  out[7] = a[7];
  return out;
}

VecP Quat2_length(Quat2 a){
    return sqrt((a[0]*a[0] + a[1]*a[1] + a[2]*a[2] + a[3]*a[3]));
}



Quat2 Quat2_normalize(Quat2 out, Quat2 a) {
  VecP magnitude = Quat2_squaredLength(a);
  if (magnitude > 0) {
    magnitude = sqrt(magnitude);

    VecP a0 = a[0] / magnitude;
    VecP a1 = a[1] / magnitude;
    VecP a2 = a[2] / magnitude;
    VecP a3 = a[3] / magnitude;

    VecP b0 = a[4];
    VecP b1 = a[5];
    VecP b2 = a[6];
    VecP b3 = a[7];

    VecP a_dot_b = (a0 * b0) + (a1 * b1) + (a2 * b2) + (a3 * b3);

    out[0] = a0;
    out[1] = a1;
    out[2] = a2;
    out[3] = a3;

    out[4] = (b0 - (a0 * a_dot_b)) / magnitude;
    out[5] = (b1 - (a1 * a_dot_b)) / magnitude;
    out[6] = (b2 - (a2 * a_dot_b)) / magnitude;
    out[7] = (b3 - (a3 * a_dot_b)) / magnitude;
  }
  return out;
}

bool Quat2_equals(Quat2 a, Quat2 b) {
  return a[0] == b[0] && a[1] == b[1] && a[2] == b[2] && a[3] == b[3] &&
    a[4] == b[4] && a[5] == b[5] && a[6] == b[6] && a[7] == b[7];
}