Quat2 Quat2_create() {
  Quat2 dq = malloc(sizeof(VecP) * 8);
  
    dq[0] = 0;
    dq[1] = 0;
    dq[2] = 0;
    dq[4] = 0;
    dq[5] = 0;
    dq[6] = 0;
    dq[7] = 0;

  dq[3] = 1;
  return dq;
}

Quat2 Quat2_fromValues( VecP x1, VecP y1, VecP z1, VecP w1, VecP x2, VecP y2, VecP z2, VecP w2){
  Quat2 out = malloc(sizeof(VecP) * 8);
  out[0] = x1;
  out[1] = y1;
  out[2] = z1;
  out[3] = w1;
  out[4] = x2;
  out[5] = y2;
  out[6] = z2;
  out[7] = w2;

  return out;
}

Quat2 Quat2_fromRotationTranslationValues(VecP x1,VecP y1,VecP z1,VecP w1,VecP x2,VecP y2,VecP z2) {
  Quat2 dq = malloc(sizeof(VecP) * 8);
  dq[0] = x1;
  dq[1] = y1;
  dq[2] = z1;
  dq[3] = w1;
  VecP ax = x2 * 0.5,
    ay = y2 * 0.5,
    az = z2 * 0.5;
  dq[4] = ax * w1 + ay * z1 - az * y1;
  dq[5] = ay * w1 + az * x1 - ax * z1;
  dq[6] = az * w1 + ax * y1 - ay * x1;
  dq[7] = -ax * x1 - ay * y1 - az * z1;
  return dq;
}


Quat2 Quat2_fromRotationTranslation(Quat2 out, Quat q,Vec3 t) {
  VecP ax = t[0] * 0.5,
    ay = t[1] * 0.5,
    az = t[2] * 0.5,
    bx = q[0],
    by = q[1],
    bz = q[2],
    bw = q[3];
  out[0] = bx;
  out[1] = by;
  out[2] = bz;
  out[3] = bw;
  out[4] = ax * bw + ay * bz - az * by;
  out[5] = ay * bw + az * bx - ax * bz;
  out[6] = az * bw + ax * by - ay * bx;
  out[7] = -ax * bx - ay * by - az * bz;
  return out;
}

Quat2 Quat2_fromTranslation(Quat2 out,Vec3 t) {
  out[0] = 0;
  out[1] = 0;
  out[2] = 0;
  out[3] = 1;
  out[4] = t[0] * 0.5;
  out[5] = t[1] * 0.5;
  out[6] = t[2] * 0.5;
  out[7] = 0;
  return out;
}

